<table width="95%" align="center"><tr><td>The Health Insurance Portability and Accountability Act (HIPAA) was signed into law in 1996. The federal regulation includes mandates that set standards for protecting the privacy of medical and health information; now referred to as Protected Health Information (PHI). PHI is information that is maintained by healthcare entities, for example: hospitals, pharmacies, health plans, dental practices, and private physician practices. <br><br>
 
HIPAA requires covered entities to take steps to limit the use and disclosure of PHI, which means that healthcare entities will be required to develop new policies and procedures that address protecting the privacy and security of patient information. Civil and criminal penalties have been established if covered entities violate patients rights. The Office of Civil Rights is the oversight agency responsible for HIPAA compliancy.<br><br>
The deadline for compliance with the privacy standards was April 14, 2003 (small health plans have until April 14, 2004). To insure compliance, healthcare entities will be required to document (forms) information captured as patients exercise their privacy rights.
